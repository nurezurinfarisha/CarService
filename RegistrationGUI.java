    private void cboTypeOfServiceItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboTypeOfServiceItemStateChanged
        // TODO add your handling code here:
        double priceInterim = 237.02;
        double priceFull = 583.79;
        double priceMajor = 1043.02;

        if (cboTypeOfService.getSelectedItem().equals("Interim Car Service")) {
            txtCost.setText("" + priceInterim);
        } else if (cboTypeOfService.getSelectedItem().equals("Full Car Service")) {
            txtCost.setText("" + priceFull);
        } else if (cboTypeOfService.getSelectedItem().equals("Major Car Service")) {
            txtCost.setText("" + priceMajor);
        }
    }//GEN-LAST:event_cboTypeOfServiceItemStateChanged

