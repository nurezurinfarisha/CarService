    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableRegisteration.getModel();
        if (jTableRegisteration.getSelectedRowCount() == 1) {
            String carModel = txtCarModel.getText();
            String carType = cboCarType.getSelectedItem().toString();
            String carColor = cboCarColor.getSelectedItem().toString();
            String carPlate = txtCarPlate.getText();
            String carService = cboTypeOfService.getSelectedItem().toString();
            String cost = txtCost.getText();
            String dateService = dFormat.format(jDateChooser1.getDate());
            String timeService = txtTimeOfServices.getText();
            String name = txtCustomerName.getText();
            String phoneNo = txtTelephoneNo.getText();
            String gender = cboGender.getSelectedItem().toString();
            String email = txtEmail.getText();
            String address = txtAddress.getText();

            model.setValueAt(carModel, jTableRegisteration.getSelectedRow(), 0);
            model.setValueAt(carType, jTableRegisteration.getSelectedRow(), 1);
            model.setValueAt(carColor, jTableRegisteration.getSelectedRow(), 2);
            model.setValueAt(carPlate, jTableRegisteration.getSelectedRow(), 3);
            model.setValueAt(carService, jTableRegisteration.getSelectedRow(), 4);
            model.setValueAt(cost, jTableRegisteration.getSelectedRow(), 5);
            model.setValueAt(dateService, jTableRegisteration.getSelectedRow(), 6);
            model.setValueAt(timeService, jTableRegisteration.getSelectedRow(), 7);
            model.setValueAt(name, jTableRegisteration.getSelectedRow(), 8);
            model.setValueAt(phoneNo, jTableRegisteration.getSelectedRow(), 9);
            model.setValueAt(email, jTableRegisteration.getSelectedRow(), 10);
            model.setValueAt(address, jTableRegisteration.getSelectedRow(), 11);
            model.setValueAt(gender, jTableRegisteration.getSelectedRow(), 12);

            JOptionPane.showMessageDialog(this, "Update Successfully");
        } else {
            if (jTableRegisteration.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table is Empty");
            } else {
                JOptionPane.showMessageDialog(this, "Please select single row for update");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

